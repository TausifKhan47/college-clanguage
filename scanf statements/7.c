//Input two numbers and prints its Addition, Subtraction, Multiplication, and Division.
#include<stdio.h>
int main()
{
    int a,b;
    printf("enter the value of a and b:");
    scanf("%d%d",&a,&b);
    printf("The addition of the numbers is %d\n",a+b);
    printf("The subtraction of the numbers is %d\n",a-b);
    printf("The multiplication of the numbers is %d\n",a*b);
    printf("The division of the numbers is %d",a/b);
    
}